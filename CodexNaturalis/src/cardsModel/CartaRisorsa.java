package cardsModel;

public class CartaRisorsa extends Carta{
	private String tipoRegno;
	private int punti;
	
	
	
	public String getTipoRegno() {
		return tipoRegno;
	}
	public void setTipoRegno(String tipoRegno) {
		this.tipoRegno = tipoRegno;
	}
	
	
	public int getPunti() {
		return punti;
	}
	public void setPunti(int punti) {
		this.punti = punti;
	}
	
	
}
