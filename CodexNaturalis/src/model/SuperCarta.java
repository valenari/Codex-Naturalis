package model;

public class SuperCarta {
		
		public String tipoCarta;
		public boolean angoloCarta;
		public int [][] posizioneCarta;
		
		public SuperCarta(String tipoCarta, boolean angoloCarta, int[][] posizioneCarta) {
			this.tipoCarta=tipoCarta;
			this.angoloCarta=angoloCarta;
			this.posizioneCarta=posizioneCarta;
		}
		
}
